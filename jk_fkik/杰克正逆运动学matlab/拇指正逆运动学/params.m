% ================================================================================
%                         灵巧手拇指机构参数定义文件
% ================================================================================
% 说明：本文件包含拇指正逆运动学求解所需的所有结构常数和配置参数
% 修改此文件中的参数将同时影响正解和逆解计算
% ================================================================================

function p = params()
    % PARAMS 返回拇指机构参数结构体
    
    %% ==================== 结构常数 ====================
    % 这些参数由机械设计决定，单位：mm 或 度
    
    % 固定偏角 (度)
    p.alpha1 = -3.65;      % 基座固定偏角
    p.alpha2 = 8.22;       % M 系固定偏角  
    p.alpha3 = -16.72;     % N 系固定偏角
    
    % 连杆长度 (mm)
    p.L1 = hypot(23.51, 1.5);  % OM 长度 ≈ 23.56 mm
    p.L2 = 53.17;              % MN 长度 (mm)
    p.L3 = 25.57;              % NT 长度 (mm)
    
    %% ==================== 关节限位 (度) ====================
    % 各关节的物理运动范围限制
    
    % q1: 基座关节 1
    p.q1_limit_min = -95;
    p.q1_limit_max = 0;
    
    % q2: 基座关节 2
    p.q2_limit_min = -55;
    p.q2_limit_max = 0;
    
    % q3: 中间关节
    p.q3_limit_min = -86;
    p.q3_limit_max = 0;
    
    % q4: 末端关节
    p.q4_limit_min = -70;
    p.q4_limit_max = 0;
    
    % 关节限位检查容差 (度)
    p.limit_tolerance = 0.5;
    
    %% ==================== 逆解容差参数 ====================
    % 用于逆解求解时的数值容差和目标点修正范围
    
    % 位置容差 (mm)
    p.tol_x = 0.04;
    p.tol_y = 0.04;
    p.tol_z = 0.04;
    
    % 角度容差 (度)
    p.tol_phi = 0.5;  % 增加容差以提高求解成功率
    
    % 法向量容差
    % 根据 tol_phi=0.5° 计算：sin(0.5°) ≈ 0.0087
    p.tol_normal = 0.01;  % 法向量分量容差
    
    % 混合策略阈值
    p.delta_analytic_limit = -5000;   % delta > 此值时用解析法
    p.delta_hard_limit = -80000;      % delta < 此值时直接报错
    
    %% ==================== 验证容差 ====================
    % 用于正逆解验证的误差阈值
    
    % 位置验证容差 (mm)
    p.position_tolerance = 0.01;
    
    % 共面性验证容差
    p.coplanar_tolerance = 1e-6;
    
    % d_y 验证容差 (MT 向量在{1}系中的 Y 分量)
    p.dy_tolerance = 1e-6;
    
    %% ==================== 平面夹角范围 ====================
    % phi 的有效范围 (度)
    p.phi_min = 0;
    p.phi_max = 90;
end
