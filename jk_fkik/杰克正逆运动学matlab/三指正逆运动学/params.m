function params = params()
%IK_PARAMS ILDA 手 MCP 关节参数配置文件
%   返回包含所有机构参数的结构体
% （集中管理、方便传递、溯源归属、字段可以不同类型、可动态添加）
    %调用：p = params();     ← 调用函数，返回结构体
    %p.ax             ← 访问结构体字段   
    %% ==================== 几何参数 (单位：mm) ====================
    
    % 基指节参数
    params.ax = 8.82;         % 基座 X 方向半宽
        %. 的含义：字段访问运算符
    params.ay = 8;            % 基座 Y 方向偏移
    params.bx = 9;         % 动平台 X 方向半宽
    params.by = 5.66;         % 动平台 Y 方向偏移
    params.bz = 5.66;         % 动平台 Z 方向偏移
    params.p = 27.98;         % 并联机构初始高度
    
    % 支链长度
    params.l1 = 13;           % 支链 1 长度(B1-C1 连杆)
    params.l2 = 13;           % 支链 2 长度(B2-C2 连杆)，用于并联机构 d1/d2 求解
    
    %% ==================== 三杆机构参数 (单位：mm) ====================
    
    % 杆长（统一字段名 L_XXX）
    params.L_PM = 42.39;               %sqrt(42.2^2 + 4^2)PM 杆长 ≈ 42.39
    params.L_MN = 30;                   % MN 杆长
    params.L_NT = 24.16;                % NT 杆长
    
    %% ==================== 四连杆机构参数 (单位：mm/度) ====================
    % 用于 q3_to_q4 函数
    params.L_NQ = 4.0;          % NQ 杆长
    params.L_KQ = 27.5;         % KQ 杆长
    params.MK_LEN = 5.0;        % MK 距离
    params.MK_ANGLE = -80.0;    % MK 与水平线夹角（度）
    params.PHI = 141.89;        % ∠QNT 固定角（度）
    
    %% ==================== P 点坐标 (单位：mm) ====================
    % P 点是空间固定点，可根据实际安装位置修改
    params.P = [0, 0, params.p];        % [Px, Py, Pz]
    
    %% ==================== 初始角度 (单位：度) ====================
    % 统一使用度 (deg)，便于阅读和修改
    params.theta3_init = 5.41;          % M 点初始角（MN 相对 PM 延长线）
    params.theta4_init = -8.15;         % N 点初始角（NT 相对 MN 延长线）
    
    %% ==================== q3-q4 耦合关系 ====================
    % 方法 1、2 使用 q3_to_q4 函数，方法 3 使用线性耦合
    % 建议统一为函数句柄
    
    %函数句柄是 MATLAB 中一种特殊的数据类型，用于引用函数。
    % 它允许你将函数作为变量传递、存储和调用。

    params.q3_to_q4_func = @q3_to_q4;   % 引用耦合函数
        
    %% ==================== 关节搜索范围 (单位：度) ====================
    % 统一使用度 (deg)
    params.q1_min = -90;                % q1(屈伸角) 最小值
    params.q1_max = 15;                 % q1(屈伸角) 最大值
    params.q2_min = -45;                % q2(外展角) 最小值
    params.q2_max = 45;                 % q2(外展角) 最大值
    
    params.q3_min = -90;                % q3 最小值（根据实际机构限位）
    params.q3_max = 15;                 % q3 最大值
    
    %% ==================== 求解容差 ====================
    params.tolerance = 1e-8;            % 数值求解收敛容差
    
    %% ==================== OP 距离验证 ====================
    params.L_OP = 27.98;                % OP 距离（验证 P 点位置）
end
